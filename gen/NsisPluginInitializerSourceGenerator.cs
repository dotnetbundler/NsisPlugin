using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.Text;

namespace NsisPlugin.SourceGeneration;

[Generator]
public class NsisPluginInitializerSourceGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // 创建一个增量节点：判断当前项目是否支持 ModuleInitializer
        var isSupportedProvider = context.CompilationProvider.Select((compilation, _) =>
        {
            // 检查 C# 语言版本是否满足最低要求 (C# 9.0)
            if (compilation is CSharpCompilation { LanguageVersion: < LanguageVersion.CSharp9 }) return false;

            // 检查目标框架（或引用的库）中是否存在 ModuleInitializerAttribute
            return compilation.GetTypeByMetadataName("System.Runtime.CompilerServices.ModuleInitializerAttribute") is not null;
        });

        context.RegisterSourceOutput(isSupportedProvider, static (spc, isSupported) =>
        {
            if (!isSupported) return;

            const string sourceCode =
                """
                // <auto-generated/>
                using System;
                using System.Reflection;
                using System.Runtime.CompilerServices;
                using System.Runtime.InteropServices;
                using NsisPlugin;

                namespace AutoGenerated
                {
                    internal static class NsisPluginInitializer
                    {
                        [ModuleInitializerAttribute]
                        internal static void Initialize()
                        {
                            string currentModuleName = $"{Assembly.GetExecutingAssembly().GetName().Name}.dll";

                            NsPluginEnc.IsGlobalUnicode = true;
                            NsPlugin.ModuleHandle = GetModuleHandle(currentModuleName);

                            [DllImport("kernel32.dll")]
                            static extern IntPtr GetModuleHandle(string lpModuleName);
                        }
                    }
                }
                """;

            spc.AddSource("NsisPluginInitializer.g.cs", SourceText.From(sourceCode, Encoding.UTF8));
        });
    }
}
