using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.Diagnostics;
using NsisPlugin.SourceGeneration.Diagnostics;
using NsisPlugin.SourceGeneration.Model;

namespace NsisPlugin.SourceGeneration.Parser;

internal static class InitializerParser
{
    private const string AutoGeneratePropertyKey = "build_property.AutoGenerateNsisPluginInitializer";

    public static InitializerParseResult Parse(AnalyzerConfigOptionsProvider optionsProvider, Compilation compilation)
    {
        if (!optionsProvider.GlobalOptions.TryGetValue(AutoGeneratePropertyKey, out var autoGenerateEnabled) || !string.Equals(autoGenerateEnabled, "true", StringComparison.OrdinalIgnoreCase))
        {
            return new InitializerParseResult([InitializerDiagnostics.CreateSkipped(InitializerSkipReason.AutoGenerateDisabled)]);
        }

        if (compilation is not CSharpCompilation { LanguageVersion: >= LanguageVersion.CSharp9 })
        {
            return new InitializerParseResult([InitializerDiagnostics.CreateSkipped(InitializerSkipReason.LanguageVersionNotSupported)]);
        }

        if (compilation.GetTypeByMetadataName("System.Runtime.CompilerServices.ModuleInitializerAttribute") is null)
        {
            return new InitializerParseResult([InitializerDiagnostics.CreateSkipped(InitializerSkipReason.MissingModuleInitializerAttribute)]);
        }

        return default;
    }
}
