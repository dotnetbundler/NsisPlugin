using Microsoft.CodeAnalysis;
using SourceGenerators;

namespace NsisPlugin.SourceGeneration.Diagnostics;

internal static class InitializerDiagnostics
{
    private static readonly DiagnosticDescriptor _languageVersionNotSupported = DiagnosticCatalog.CreateDescriptor(
        "NSPGEN001",
        "C# language version is not supported",
        "Source generation requires C# 9.0 or later",
        DiagnosticSeverity.Warning);

    private static readonly DiagnosticDescriptor _missingModuleInitializerAttribute = DiagnosticCatalog.CreateDescriptor(
        "NSPGEN002",
        "ModuleInitializerAttribute is unavailable",
        "Source generation requires System.Runtime.CompilerServices.ModuleInitializerAttribute",
        DiagnosticSeverity.Warning);

    private static readonly DiagnosticDescriptor _autoGenerateDisabled = DiagnosticCatalog.CreateDescriptor(
        "NSPGEN003",
        "Initializer generation is disabled",
        "Source generation is skipped because MSBuild property 'AutoGenerateNsisPluginInitializer' is not set to 'true'",
        DiagnosticSeverity.Info);

    private static DiagnosticDescriptor GetDescriptor(InitializerSkipReason reason) => reason switch
    {
        InitializerSkipReason.LanguageVersionNotSupported => _languageVersionNotSupported,
        InitializerSkipReason.MissingModuleInitializerAttribute => _missingModuleInitializerAttribute,
        _ => _autoGenerateDisabled
    };

    public static DiagnosticInfo CreateSkipped(InitializerSkipReason reason) => DiagnosticInfo.Create(GetDescriptor(reason), Location.None, []);
}

internal enum InitializerSkipReason
{
    LanguageVersionNotSupported,
    MissingModuleInitializerAttribute,
    AutoGenerateDisabled
}
