using Microsoft.CodeAnalysis;
using SourceGenerators;

namespace NsisPlugin.SourceGeneration.Diagnostics;

internal static class InitializerDiagnostics
{
    private const string Category = "NsisPlugin.SourceGeneration";

    private static readonly DiagnosticDescriptor _languageVersionNotSupported = new(
        "NSISPLUGINGEN001",
        "Language version is not supported",
        "Nsis plugin initializer source generation requires C# 9.0 or later",
        Category,
        DiagnosticSeverity.Info,
        true);

    private static readonly DiagnosticDescriptor _missingModuleInitializerAttribute = new(
        "NSISPLUGINGEN002",
        "ModuleInitializerAttribute is unavailable",
        "Nsis plugin initializer source generation requires System.Runtime.CompilerServices.ModuleInitializerAttribute",
        Category,
        DiagnosticSeverity.Info,
        true);

    private static readonly DiagnosticDescriptor _autoGenerateDisabled = new(
        "NSISPLUGINGEN003",
        "Initializer generation is disabled",
        "Nsis plugin initializer source generation is skipped because AutoGenerateNsisPluginInitializer is not set to true",
        Category,
        DiagnosticSeverity.Info,
        true);

    public static DiagnosticDescriptor GetDescriptor(InitializerSkipReason reason) => reason switch
    {
        InitializerSkipReason.LanguageVersionNotSupported => _languageVersionNotSupported,
        InitializerSkipReason.MissingModuleInitializerAttribute => _missingModuleInitializerAttribute,
        _ => _autoGenerateDisabled
    };

    public static DiagnosticInfo CreateSkipped(InitializerSkipReason reason) => DiagnosticInfo.Create(GetDescriptor(reason), Location.None, []);
}

internal enum InitializerSkipReason
{
    LanguageVersionNotSupported,
    MissingModuleInitializerAttribute,
    AutoGenerateDisabled
}
