using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.Text;
using NsisPlugin.SourceGeneration.Model;
using NsisPlugin.SourceGeneration.Parser;
using SourceGenerators;

namespace NsisPlugin.SourceGeneration.Emitters;

internal class ExportEmitter(SourceProductionContext context)
{
    // 源生成用到的字面量
    private const string StackTopName = "StackTop";
    private const string VariablesName = "Variables";
    private const string ExtraParametersName = "ExtraParameters";

    // 引用类的 global::fully.qualified.name
    private const string IntPtrRef = "global::System.IntPtr";
    private const string ExceptionRef = "global::System.Exception";
    private const string DisposableRef = "global::System.IDisposable";
    private const string CallConvCdeclRef = "global::System.Runtime.CompilerServices.CallConvCdecl";
    private const string UnmanagedCallersOnlyAttributeRef = "global::System.Runtime.InteropServices.UnmanagedCallersOnlyAttribute";

    private const string EncodingsRef = "global::NsisPlugin.Encodings";
    private const string StackTRef = "global::NsisPlugin.StackT";
    private const string VariablesRef = "global::NsisPlugin.Variables";
    private const string ExtraParametersRef = "global::NsisPlugin.ExtraParameters";
    private const string NsisPluginRef = "global::NsisPlugin.NsPlugin";
    private const string NsPluginEncRef = "global::NsisPlugin.NsPluginEnc";

    // 用于类型显示的格式，包含全限定名、类型参数、特殊类型转换等选项
    private static readonly SymbolDisplayFormat _fullQualifiedFormat = new(SymbolDisplayGlobalNamespaceStyle.Included, SymbolDisplayTypeQualificationStyle.NameAndContainingTypesAndNamespaces, SymbolDisplayGenericsOptions.IncludeTypeParameters, miscellaneousOptions: SymbolDisplayMiscellaneousOptions.UseSpecialTypes | SymbolDisplayMiscellaneousOptions.IncludeNullableReferenceTypeModifier | SymbolDisplayMiscellaneousOptions.EscapeKeywordIdentifiers);

    /// <summary>
    /// 根据导出类型规范生成源代码，并将其添加到编译上下文中
    /// </summary>
    public void Emit(ImmutableEquatableArray<ExportTypeSpec> typeSpecs)
    {
        foreach (var typeSpec in typeSpecs)
        {
            var hintName = GetHintName(typeSpec.ContainingType);
            var sourceText = Emit(typeSpec);
            context.AddSource(hintName, sourceText);
        }

        static string GetHintName(INamedTypeSymbol containingType) => $"{containingType.ToDisplayString(_fullQualifiedFormat).Replace("global::", "").Replace('.', '_').Replace('+', '_')}.NsisExports.g.cs";
    }

    private static SourceText Emit(ExportTypeSpec typeSpec)
    {
        var writer = new SourceWriter();

        writer.WriteLine("// <auto-generated />");
        writer.WriteLine();

        if (!typeSpec.ContainingType.ContainingNamespace.IsGlobalNamespace)
        {
            writer.WriteLine($"namespace {typeSpec.ContainingType.ContainingNamespace.ToDisplayString()}");
            writer.WriteLine('{');
            writer.Indentation++;
        }

        // 命名空间内部
        {
            writer.WriteLine($"public static class {GetExportClassName(typeSpec.ContainingType)}");
            writer.WriteLine('{');
            writer.Indentation++;

            // 类内部
            var isFirstMethod = true;
            foreach (var methodSpec in typeSpec.Methods) WriteMethod(writer, methodSpec, ref isFirstMethod);

            writer.Indentation--;
            writer.WriteLine('}');
        }

        if (!typeSpec.ContainingType.ContainingNamespace.IsGlobalNamespace)
        {
            writer.Indentation--;
            writer.WriteLine('}');
        }
        return writer.ToSourceText();

        // 获取导出类名
        static string GetExportClassName(INamedTypeSymbol containingType)
        {
            var parts = new Stack<string>();
            for (var cur = containingType; cur is not null; cur = cur.ContainingType) parts.Push(cur.Name);
            return string.Join("_", parts) + "NsisExports";
        }
    }

    private static void WriteMethod(SourceWriter writer, ExportMethodSpec methodSpec, ref bool isFirstMethod)
    {
        var method = methodSpec.Method;
        foreach (var actionSpec in methodSpec.Actions)
        {
            // 方法间隔
            if (!isFirstMethod) writer.WriteLine();
            else isFirstMethod = false;

            writer.WriteLine($"[{UnmanagedCallersOnlyAttributeRef}(EntryPoint = {SymbolDisplay.FormatLiteral(actionSpec.EntryPoint, true)}, CallConvs = new[] {{ typeof({CallConvCdeclRef}) }})]");
            writer.WriteLine($"public static void {actionSpec.EntryPoint}_Gen({IntPtrRef} hwndParent, {IntPtrRef} string_size, {IntPtrRef} variables, {IntPtrRef} stacktop, {IntPtrRef} extra)");
            writer.WriteLine('{');
            writer.Indentation++;

            // 方法内部
            {
                writer.WriteLine("try");
                writer.WriteLine('{');
                writer.Indentation++;
                // try 内部
                {
                    writer.WriteLine($"using {DisposableRef} _ = {NsPluginEncRef}.CreateEncScope({EncodingsRef}.{actionSpec.Encoding});");
                    writer.WriteLine($"{NsisPluginRef}.Init(hwndParent, string_size, variables, stacktop, extra);");

                    // 参数
                    if (methodSpec.Method.Parameters.Length > 0) writer.WriteLine();
                    List<string> arguments = new(methodSpec.Method.Parameters.Length);
                    foreach (var parameter in methodSpec.Method.Parameters)
                    {
                        if (TryGetSpecialArgument(parameter, out var argument))
                        {
                            arguments.Add(argument!);
                            continue;
                        }

                        var paramName = EscapeIdentifier(parameter.Name);
                        var paramType = $"{ToNonNullableDisplayString(parameter.Type)}?";
                        if (ExportParser.TryGetFromVariableAttribute(parameter, out var fromVariable))
                        {
                            var throwCode = $"throw new {ExceptionRef}(\"Failed to get '{parameter.Name}'({paramType}) from the variable\");";
                            writer.WriteLine($"if (!{NsisPluginRef}.{VariablesName}.Get({VariablesRef}.{fromVariable}, out {paramType} {paramName})) {throwCode}");
                        }
                        else
                        {
                            var throwCode = $"throw new {ExceptionRef}(\"Failed to get '{parameter.Name}'({paramType}) from the stack\");";
                            writer.WriteLine($"if (!{NsisPluginRef}.{StackTopName}.Pop(out {paramType} {paramName})) {throwCode}");
                        }
                        var paramExpression = parameter.Type.IsValueType ? $"{paramName}.Value" : $"{paramName}!";
                        arguments.Add(paramExpression);
                    }

                    // 调用方法
                    writer.WriteLine();
                    var invocation = $"{method.ContainingType.ToDisplayString(_fullQualifiedFormat)}.{method.Name}({string.Join(", ", arguments)});";
                    if (method.ReturnsVoid) writer.WriteLine(invocation);
                    else
                    {
                        writer.WriteLine($"{method.ReturnType.ToDisplayString(_fullQualifiedFormat)} result = {invocation}");

                        // 推送结果
                        writer.WriteLine(ExportParser.TryGetToVariableAttribute(method, out var toVariable) ?
                            $"{NsisPluginRef}.{VariablesName}.Set({VariablesRef}.{toVariable}, result);" :
                            $"{NsisPluginRef}.{StackTopName}.Push(result);");
                    }
                }
                writer.Indentation--;
                writer.WriteLine('}');
                writer.WriteLine($"catch ({ExceptionRef} ex)");
                writer.WriteLine('{');
                writer.Indentation++;
                // catch 内部
                {
                    writer.WriteLine($"{NsisPluginRef}.{StackTopName}.Push($\"Exception in {actionSpec.EntryPoint}: {{ex}}\");");
                }
                writer.Indentation--;
                writer.WriteLine('}');
            }

            writer.Indentation--;
            writer.WriteLine('}');
        }

        // 获取特殊参数
        static bool TryGetSpecialArgument(IParameterSymbol parameter, out string? argument)
        {
            argument = ToNonNullableDisplayString(parameter.Type) switch
            {
                VariablesRef => $"{NsisPluginRef}.{VariablesName}",
                StackTRef => $"{NsisPluginRef}.{StackTopName}",
                ExtraParametersRef => $"{NsisPluginRef}.{ExtraParametersName}",
                _ => null
            };
            return argument != null;
        }
    }

    /// <summary>
    /// 判断类型是否为可空类型
    /// </summary>
    private static bool IsNullable(ITypeSymbol type)
    {
        // 检查值类型（int?）
        if (type.IsValueType) return type.OriginalDefinition.SpecialType == SpecialType.System_Nullable_T;

        // 检查引用类型（string?）
        return type.NullableAnnotation == NullableAnnotation.Annotated;
    }

    /// <summary>
    /// 获取类型的非可空版本
    /// </summary>
    private static ITypeSymbol GetNonNullableType(ITypeSymbol type)
    {
        // 处理值类型：如果是 int?，返回内部的 int
        if (type.IsValueType && type is INamedTypeSymbol { OriginalDefinition.SpecialType: SpecialType.System_Nullable_T } namedType) return namedType.TypeArguments[0];

        // 处理引用类型：如果是 string?，返回标注为 NonNullable 的 string
        if (type.NullableAnnotation == NullableAnnotation.Annotated) return type.WithNullableAnnotation(NullableAnnotation.NotAnnotated);

        return type;
    }

    /// <summary>
    /// 获取类型的非可空显示字符串
    /// </summary>
    private static string ToNonNullableDisplayString(ITypeSymbol type) => GetNonNullableType(type).ToDisplayString(_fullQualifiedFormat);

    /// <summary>
    /// 如果标识符是 C# 关键字，则在前面加 @ 转义，否则返回原字符串
    /// </summary>
    private static string EscapeIdentifier(string name) => SyntaxFacts.GetKeywordKind(name) != SyntaxKind.None ? $"@{name}" : name;
}
