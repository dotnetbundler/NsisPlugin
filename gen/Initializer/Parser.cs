using System.Collections.Immutable;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.Diagnostics;

namespace NsisPlugin.SourceGeneration.Initializer;

internal static class Parser
{
    private const string GenerateInitializerPropertyKey = "build_property.NsisPluginGenerateInitializer";
    private const string ModuleInitializerAttributeFullName = "System.Runtime.CompilerServices.ModuleInitializerAttribute";

    public static (bool, ImmutableArray<Diagnostic>) Parse(AnalyzerConfigOptionsProvider optionsProvider, Compilation compilation)
    {
        List<Diagnostic> diagnostics = [];

        if (compilation is not CSharpCompilation { LanguageVersion: >= LanguageVersion.CSharp9 })
        {
            diagnostics.Add(Diagnostic.Create(DiagnosticDescriptors.LanguageVersionNotSupported, Location.None));
            return (false, [..diagnostics]);
        }

        if (compilation.GetTypeByMetadataName(ModuleInitializerAttributeFullName) is null)
        {
            diagnostics.Add(Diagnostic.Create(DiagnosticDescriptors.MissingModuleInitializerAttribute, Location.None));
            return (false, [..diagnostics]);
        }

        if (!optionsProvider.GlobalOptions.TryGetValue(GenerateInitializerPropertyKey, out var generateInitializerValue) || !bool.TryParse(generateInitializerValue, out var generateInitializer) || !generateInitializer)
        {
            diagnostics.Add(Diagnostic.Create(DiagnosticDescriptors.AutoGenerateDisabled, Location.None));
            return (false, [..diagnostics]);
        }

        return (true, [..diagnostics]);
    }
}
